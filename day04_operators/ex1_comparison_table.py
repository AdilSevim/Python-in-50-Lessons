"""
Day 4, Example 1: Comparison Table

Purpose: Show the results of various comparison operations.
We use comparison operators to compare values and display the results.

Expected output when run:
Comparison Table:
5 == 3 : False
5 != 3 : True
5 < 3 : False
5 > 3 : True
5 <= 3 : False
5 >= 3 : True
"""

# Define values to compare
a = 5
b = 3

# Display a comparison table
print("Comparison Table:")
print(a, "==", b, ":", a == b)
print(a, "!=", b, ":", a != b)
print(a, "<", b, ":", a < b)
print(a, ">", b, ":", a > b)
print(a, "<=", b, ":", a <= b)
print(a, ">=", b, ":", a >= b)
